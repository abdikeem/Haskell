{-
Topic: Types and typeclasses
We can use :type or :t to find out the type of a value
eg:
ghci> :t "kimza" // OUTPUT = "kimza" :: String

NB: While using Double quotes("") the output will be string and single quotes('') the output is char in single letter.

Unlike Java or Pascal, Haskell has type inference.
NOTE: Explicit types are always denoted with the first letter in capital case.

-}